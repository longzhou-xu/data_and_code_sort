    for N = 1 : length(Tau_rs) 
        if P_alpha_rs(M) > 0.1 && P_tau_rs(N) > 0.1
           Gamma_alpha_tau(M,N) = (Tau_rs(N)-1) / (Alpha_rs(M)-1);
           Gamma_fit_1(M,N) = Gamma_fit_rs(N);
           DLAT(M,N) = abs(Gamma_alpha_tau(M,N)-Gamma_fit_1(M,N));
        else
           Gamma_alpha_tau(M,N) = -0.1;
           Gamma_fit_1(M,N) = -0.1;
           DLAT(M,N) = -0.1;
        end
    end
