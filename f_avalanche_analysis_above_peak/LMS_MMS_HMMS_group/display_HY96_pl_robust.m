for M = 1 : length(Alpha_rs)
    for N = 1 : length(Tau_rs) 
       Gamma_alpha_tau(M,N) = (Tau_rs(N)-1) / (Alpha_rs(M)-1);
       Gamma_fit_1(M,N) = Gamma_fit_rs(N);
       II = II + 1;
       delta_LMS(II,1) = abs(Gamma_alpha_tau(M,N)-Gamma_fit_1(M,N));
    end
end
save('DELTA_LMS.mat','delta_LMS')

