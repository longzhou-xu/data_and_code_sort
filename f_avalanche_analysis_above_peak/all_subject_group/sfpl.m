         'Interpreter','latex',...
         'Color', [0.00,0.00,0.00], ...
         'FontName', 'Arial', ...
         'FontSize', 20, ...
         'EdgeColor','none');
     xlabel('avalanche size S', ...
            'FontName', 'Arial');
     ylabel('P(S)', ...
            'FontName', 'Arial');
     set(gca, ...
         'FontName', 'Arial', ...
         'FontSize',18, ...
         'XTick', [1,10,100,1000], ...
