load('BN_246\Avalanches_1.7.mat')
AvaDuration =  AvaD_AllSubject;
AvaSize = AvaS_AllSubject;
SizeFitRange = [7,40];
DurationFitRange = [3,10];
% average size across avalanche with same lifetime
[Lifetime, AverageSize]=lifetimeAverageSize(AvaDuration, AvaSize);
% calculate the distribution of avalanche size and duration
    SizeRange = min(AvaSize) : 1 : max(AvaSize);
    AvaSizeHist = hist(AvaSize, SizeRange);
    AvaSizeHist = AvaSizeHist/AvaSizeHist(1);
    
    DurationRange = min(AvaDuration) : 1 : max(AvaDuration);
    AvaDurationHist = hist(AvaDuration,DurationRange);
    AvaDurationHist = AvaDurationHist/AvaDurationHist(1);
% power law fit
    [Alpha, Smin2fit, Smax2fit] = plmle(AvaSize, 'xmin', SizeFitRange(1), 'xmax', SizeFitRange(2));
    [P_alpha] = pvcalc(AvaSize, Alpha, 'xmin',Smin2fit,'xmax',Smax2fit);
    SizePLFitRange = Smin2fit:Smax2fit;
    SizePLFitDistribution = (Smin2fit:Smax2fit).^(-Alpha);

    [Tau, Tmin2fit, Tmax2fit] = plmle(AvaDuration,'xmin', DurationFitRange(1), 'xmax', DurationFitRange(2));
    [P_tau] = pvcalc(AvaDuration, Tau, 'xmin',Tmin2fit,'xmax',Tmax2fit);
    DurationPLFitRange = Tmin2fit:Tmax2fit;
    DurationPLFitDistribution = (Tmin2fit:Tmax2fit).^(-Tau);
        
    [Gamma_nlsfit] = createFit_pl_nonlinearLS(Lifetime(Tmin2fit:Tmax2fit), AverageSize(Tmin2fit:Tmax2fit));
    Gamma_fit = Gamma_nlsfit.b;
    Gamma_alpga_tau = (Tau-1) / (Alpha-1);
    LifetimeFitRange = Tmin2fit:Tmax2fit;
    AverageSizeFit = (Gamma_nlsfit.a).*(Tmin2fit:Tmax2fit).^(Gamma_fit);
% plot the distribution of avalanche size and duration
  AX1 = subplot(3,3,1);
    loglog(SizeRange, AvaSizeHist, ...
    'Color', [0.50, 0.50, 0.50], ...
    'LineStyle', 'none', ...
